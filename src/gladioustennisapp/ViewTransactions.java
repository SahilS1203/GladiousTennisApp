    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        DefaultTableModel tblModel1 = (DefaultTableModel)jTable1.getModel();
        DefaultTableModel tblModel2 = (DefaultTableModel)jTable2.getModel();
        tblModel1.setRowCount(0);
        tblModel2.setRowCount(0);
        String inflowstatus="";
        int sum1=0;
        int sum2=0;
        try
        {   
            Class.forName("com.mysql.cj.jdbc.Driver"); 
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:"+Utility.dbport+"/tennis",Utility.dbusername,Utility.dbpassword);
            Statement st = con.createStatement();
            String dateStartString = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
            String dateEndString = ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText();
            String sql="select * from transaction where date between '"+dateStartString+"' and '"+dateEndString+"'";
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()){
                int inflow = rs.getInt("inflow");
                String date = rs.getString("date");
                String name = rs.getString("name");
                int amt = rs.getInt("totalamount");
                String amtS = String.valueOf(amt);
                if (inflow==0){
                        inflowstatus="No";
                        String tbData[] = {name, date, amtS};
                        tblModel2.addRow(tbData);
                        sum2+=amt;
                    }
                else if (inflow==1) {
                        inflowstatus="Yes";
                        String tbData[] = {name, date, amtS};
                        tblModel1.addRow(tbData);
                        sum1+=amt;
                    }
                int cashflow= sum1-sum2;
                sumExpenditure.setText(String.valueOf(sum2));
                sumIncome.setText(String.valueOf(sum1));
                cashFlow.setText(String.valueOf(cashflow));
            }
            
            
            
            con.close();
        }
        catch(SQLException se){
             se.printStackTrace();
        }
        catch(Exception e){
            e.printStackTrace();     
        } 
     
    }//GEN-LAST:event_viewBtnActionPerformed

