    private void loadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadBtnActionPerformed
      try
            {   
            Class.forName("com.mysql.cj.jdbc.Driver"); 
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:"+Utility.dbport+"/tennis",Utility.dbusername,Utility.dbpassword);
            Statement st = con.createStatement();
            String qry="select * from transaction where name LIKE '%"+nameCombo.getSelectedItem()+"%'";
            ResultSet rs = st.executeQuery(qry);
            while (rs.next()){
                String id = String.valueOf(rs.getInt("transactionid"));
                int inflow = rs.getInt("inflow");
                Date date = rs.getDate("date");
                int amt = rs.getInt("totalamount");
                String amtS = String.valueOf(amt);
                if (inflow==0){
                      jRadioButton1.setSelected(false);  
                      jRadioButton2.setSelected(true);  
                    }
                else if (inflow==1) {
                    jRadioButton2.setSelected(false);  
                    jRadioButton1.setSelected(true);  
                        
                    }
                idTxtField.setText(id);
                amtTxtField.setText(amtS);
                jDateChooser.setDate(date);
            }
            con.close();
        
     
    }   catch (SQLException ex) {  
            Logger.getLogger(EditTransactions.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(EditTransactions.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_loadBtnActionPerformed

