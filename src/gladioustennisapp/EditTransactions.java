    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
         int amount= 0;  
         Date a = jDateChooser.getDate();
        
            if ((amtTxtField.getText().equals("")) || (a==null) || (buttonGroup1.getSelection()==null) )    {
                JOptionPane.showMessageDialog(null, "Please fill in all the fields.");
            }
            else if ((Integer.parseInt(amtTxtField.getText()))<0){
                JOptionPane.showMessageDialog(null, "Ensure the amount is positive.");
            }  
            else{
            
            amount = Integer.parseInt(amtTxtField.getText());
            int inflowDb=-1;
            String x = String.format("%1$tY-%1$tm-%1$td",a);
            
              try {
              Class.forName("com.mysql.cj.jdbc.Driver");
              Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:"+Utility.dbport+"/tennis",Utility.dbusername,Utility.dbpassword);
              if (jRadioButton1.isSelected()){
                 inflowDb=1;
              }
              else if (jRadioButton2.isSelected()){
                 inflowDb=0;
              }
               
                String qry="update transaction set inflow="+inflowDb+" , date='"+x+"' , totalamount="+amount+" where transactionid LIKE '%"+idTxtField.getText()+"%'";
                PreparedStatement st = con.prepareStatement(qry);
                st.execute();
                st.close();
                JOptionPane.showMessageDialog(null, "The data has been updated.");
              } 
              
              catch (SQLException ex) {
             Logger.getLogger(EditTransactions.class.getName()).log(Level.SEVERE, null, ex);
             } catch (ClassNotFoundException ex) {
             Logger.getLogger(EditTransactions.class.getName()).log(Level.SEVERE, null, ex);
              }
            }    
    }//GEN-LAST:event_editBtnActionPerformed

