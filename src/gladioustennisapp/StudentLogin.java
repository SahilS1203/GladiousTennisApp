    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
     if (answer.getText().isEmpty()){
         status.setText("Please complete the verification");
     }
     else{
        String ansStr = answer.getText();
        int ans = Integer.parseInt(ansStr);
        try
        {            
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:"+Utility.dbport+"/tennis",Utility.dbusername,Utility.dbpassword);
            String query = "select  username, password from student where username LIKE '%"+username.getText()+"%'";
            PreparedStatement statement = con.prepareStatement(query);
            ResultSet result = statement.executeQuery(); 
            if (result.next())
            {   
                String dbaseUsername=result.getString("username").toString().trim();
                String dbasePassword=result.getString("password").toString().trim();
                String enteredPassword=new String(password.getText().trim());
                if((dbasePassword.equals(enteredPassword)) && (ans == num3)){
                   String qry="insert into logintracker (username) values('"+dbaseUsername+"')";
                   PreparedStatement st = con.prepareStatement(qry);
                   st.execute(); 
                   st.close();
                   new StudentDashboard().setVisible(true);
                   this.setVisible(false);
                }
                else if (ans != num3) 
  			status.setText("Your answer verification is incorrect"); 
                else if (!dbasePassword.equals(enteredPassword))
                status.setText("You have entered the wrong details");
            }        
            else
            {                  
                status.setText("This username is not registered");              
            }                      
            statement.close();
            con.close();        
        }
        catch(SQLException se){
             se.printStackTrace();
        }
        catch(Exception e){
            e.printStackTrace();    
            status.setText("Exception occurred while searching in the student table");
        } 
     }
     
    }//GEN-LAST:event_LoginActionPerformed

